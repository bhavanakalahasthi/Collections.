
public class Bid {

	private int bid;
	private int amount;
	public Bid() {
		
	}
	
	public Bid(int bid, int amount) {
		super();
		this.bid = bid;
		this.amount = amount;
	}
	public int getBid() {
		return bid;
	}
	public void setBid(int bid) {
		this.bid = bid;
	}
	public int getAmount() {
		return amount;
	}
	public void setAmount(int amount) {
		this.amount = amount;
	}
	
	
}
